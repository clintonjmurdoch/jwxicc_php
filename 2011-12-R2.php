<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Round 2 v Moonee Valley</u></b></h3>  
<br></br>  
<p>Aside from the Taylor brothers, Kale Cetinich and late inclusion Patty Lahn, combined Johnnie Walker experience in this match was just six matches, leaving a cloud of anti-lance hovering over the match. Following the early loss of Ken O'Donnell, Cetinich and Mick Pataki more than made up for it, taking the score to 80 in the 13th over. Cetinich in particular was savage on the Moonee Valley attack, striking eight 4's and a six in his 45, before holing out with a maiden half-century - not for the first time - at his mercy, ending a promising 62 run stand. Lachlan Cox struck an early boundary before succumbing to chief destroyer Polonidis, however debutant Ben Coote eased the pressure with a blazing 16. One of his two sixes was struck with such force that the ball went missing for an extended period. His dismissal started a mini collapse of 4/10; Chris Taylor showed off all his batting skills with a first ball boundary followed up by a full and straight ball penetrating his defence, Thilan Jayakodi departed two balls later and Andy Taylor almost mirrored his brother's effort by lasting two more deliveries for his 4. David Bell managed to hang on long enough for Pataki to reach a hard fought fifty, but his wicket proved to be the beginning of the end; the last three wickets fell for just four runs, with Pataki narrowly missing out on becoming the first Johnnie Walker XI player to carry his bat when he was last out.</p> 
<p>Just four overs were bowled in reply before the rain came briefly and Chris Taylor controversially offered to call the game off, which was music to the ears of Moonee Valley's captain and opening batsman, who was averaging just under six over 22 career matches. Coote's extreme pace proved almost impossible to score off, and he should have had a wicket to show for his efforts, however the club umpire turned down a stone dead lbw shout; probably in retaliation for Chris Taylor accidentally doing likewise to reprieve Pataki earlier in the match.</p>

