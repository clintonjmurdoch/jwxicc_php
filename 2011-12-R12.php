<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Round 12 v Barkly St. UCCC</u></b></h3>  
<br></br>  
<p>It was fitting that, against our long term rivals in Barkly St, we fielded our 'biggest johnnie' side (excluding Willowfest) for some time. This included unexpected callups for Rory Herd - a 2008/09 village veteran of five games - and Sean Dixon - one of many one match wonders to come out of the village. Tharindu Jayakodi and Janish Suriyaaratchchi also made returns to give the top of the table team a balanced look, albeit seemingly lacking in fast bowling depth. Despite this, Chris Taylor overlooked himself and Jayakodi for the new ball, plumping instead for Herd and Vaughan Garner. It was a dangerous call; Herd failed to turn potential into performance in his first JWXI stint with just four wickets in 29 overs, while Garner was wicketless from seven overs in two separate new ball experiments in season 2011/12.</p>
<p>Herd repaid the faith in the third over with the big wicket of Marcus Curnow, who was well caught by Taylor at a lone first slip, before enticing Kerlin into a big cover drive that Cooper Reid held on to above his head. Garner was struggling to find his length at the other end, conceding two boundaries in his second over while adjusting to his new runup and whinging to Taylor that he was bowling at the wrong end - his non preferred northern end. When he finally landed one in the right area, Narula must have felt obligated to edge it; his attempted drive nestling in Andy Taylor's safe hands at gully. So long a weakness of Johnnie Walker XI teams; now two catches behind the wicket had been grasped in the first six overs. Having now settled into a rhythm, Garner began to hit the right spots and move the ball regularly and was rewarded with a spell of 4/1 off 13 balls. First he had Maeder caught behind off a thin edge, causing the batsman to throw a tantrum and refuse to leave the crease; claims that he didn't hit it and that our appeals were actually 'cheering' that caused him to be given out falling on deaf ears. This was followed up by a return catch off a full toss and a clean bowled, which reduced the opposition to 6/27, however the fifth wicket proved just out of reach, with the last ball of his spell narrowly missing off stump.</p>
<p>Cooper Reid should have struck early in his spell, however a fairly obvious stumping (though close in Moonee Valley terms) of Koesasi was turned down. Koesasi took his chance with both hands, flogging us around the park for the second time in the season. Reid was eventually rewarded for tossing the ball up with the wicket of Kuffer, who was caught at cover or mid wicket for the third time in as many matches against JWXI. Kale Cetinich turned in a thrifty spell to keep Koesasi's aggression in check, gaining a wicket and putting so much pressure on O'Keeffe that when he finally got off the mark, he called two as he played one to Andy Taylor at third man and stuck with his call to be run out by several metres. Koesasi was the last man to fall when, after surviving another chance off Reid when Janish Suriyaaratchchi spilled a dolly at cover, he succumbed to a full legbreak.</p>
<p>Despite the early finish to the innings - eight overs before tea - Chris Taylor still decided to open with brother Andy so they could leave early for their mum's birthday. Chris fell second ball after slashing the first over slips for four, while Andy managed to make it to the fourth over. Garner and Mick Pataki steadied the situation and punished the bad ball as they cruised to a 59 run stand, before Garner fell to an excellent one handed catch by Curnow for 32 off 40 - easily his quickest innings of the season. After playing a big shot and being put down shortly after, Pataki put the cue in the rack and opted for safety and red ink as victory approached, while Suriyaaratchchi was able to get a good hit under his belt.</p>

