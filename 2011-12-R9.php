<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Round 9 v Moonee Valley</u></b></h3>  
<br></br>  
<p>Following two rain affected draws against the struggling Moonee Valley, the boys were keen to tally up a big score, so it was disappointing when the toss was lost and the Johnnies were sent into the field. Clinton Murdoch and Vaughan Garner shared the new ball impressively, however the fielding effort did not match, with the first wicket coming off the fourth chance offered. Garner frustratingly had both openers dropped in consecutive balls in the fourth over; a skier to debutant Jacob Cox at mid wicket and in the slips when Cooper Reid and Ben Carr both dived for a tough chance. Reid then dropped another tough chance at slip off Murdoch next over, before Steve Pataki stunned everyone by stretching an arm out at square leg and clutching the ball on the run (if that is the right word) to give Murdoch his first wicket. Another dropped catch by Cox - this time at gully - gave Sciacchitano a third chance, who along with Tom King took the score on to 57, before he was finally caught by a running Garner (who had taken the gloves) at backward square leg off Carr. King continued to keep the score ticking over, guiding Moonee Valley to a solid 2/81 in the 22nd over. It would take some stunningly indecisive running to remove him; despite taking off for an easy single after smacking it towards the cover boundary, his partner didn't move, allowing Chris Taylor to lob the ball to the keeper's end after first gamer Callum Ronaldson's return from the deep.</p>
<p>One bizarre breakthrough morphed into a collapse of 8/21, though stranger events lay ahead. After a wicket a piece to Carr, Reid (courtesy of a trademark sharp close in catch by Taylor at midwicket) and Taylor, Reid and Garner were denied a stumping after the square leg umpire determined Legato's toe to have been behind the line, when it had remained on the line for the entire ball. Reid would remove the same batsman just two balls later after Legato got a big bottom edge on a slog sweep which was smartly taken behind the stumps. The first incorrect decision didn't hold a candle to the events of the following over, however. Seeing Royle batting a long way out of his crease to Taylor, Garner stood up to the stumps, yet Royle did not step back. Almost to script, Taylor bowled a wide full toss that was left alone and Garner removed the bails. Initially, the controlling umpire called a no ball for off the pitch, however both Taylor and Garner said it could only be a wide as the ball never hit the ground. After the changed decision, the boys started celebrating, however the same square leg umpire called not out, despite Royle remaining a metre out of his crease for the entire event. A seething Garner had to be subdued by Taylor after he made his feelings known to the entire ground, and tensions were only eased when the square leg umpire was replaced. Murdoch and Reid wrapped up the innings to finish with three wicket hauls, leaving a target of just 103.</p>
<p>Ronaldson's aggressive approach led to his downfall for a second ball duck, bringing Kale Cetinich to the crease to join Garner. A greater emphasis on picking the right ball led to some impressive strokes, before Cetinich succumbed to one big shot too many for a run a ball 27. Carr picked up where he had left off with two boundaries in his first three balls, while Garner traded his ailing bat for the SS and immediately banged a full toss through backward point for four. When Carr departed on 68, the match was still on the line, however Cox was in no mood to hang around, smashing an unbeaten 36 off 11 balls (strike rate of 327.27) out of a 40 run stand with Garner to win the match in just 14 overs. He concluded the game in impressive fashion, hitting the final three balls into the houses behind the straight boundary.</p>

