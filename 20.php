<p>Ashley Keating was drafted into the team at a critical stage midway through the first season; primarily as a legspinner to relieve the incumbent tweaker Greg Sidebottom. He struck gold immediately, snaring 4/20 off five overs - the best ever figures on debut - to bowl the side to victory. With Simon O'Shea at the other end, Pine Lodge lost 8/21 after seemingly coasting to an easy win. Promoted to open the batting next match, Keating's 48 got the innings off to a flyer that culminated in Chris Taylor's landmark century. He enjoyed the experience so much that his bowling was quietly shelved. Following up with innings of 43 and 35 in his next two innings, Keating was able to pass 150 runs despite limited games, giving him the inaugural Batting Award. Unfortunately, in two cameo appearances for Glenroy, Keating made consecutive ducks, however he did manage to pick up two crucial wickets in the famous chase of 228 against Pascoe Vale SC.</p>
