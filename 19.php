<p>Despite lacking in obvious cricketing talent, Colin Finn rarely fails to provide a moment in a Johnnie Walker XI match, whether it is via his stonewall defence, express bowling or a handy effort in the field. At Willowfest - arguably the most pressurised tournament the team has competed in - he has lifted his game several notches, regualarly blocking out high quality bowlers while other batsmen fall around him and possessing a generally safe set of hands.</p>
<p>His short career has been like a rollercoaster. After three runs and an excellent low running catch on debut, Finn followed up with a startling spell of 3/34 off eight overs against eventual runners-up Old Students (including the scalps of eventual teammates Pete and Chris Barclay), however this was interspersed with a JWXI record five consecutive ducks, including a first baller as the last man out in the capitulation against Royal Park Reds in 2008/09. His batting average now sitting at a lowly 0.50, Finn quickly rectified this during his first Willowfest innings by smiting a full ball off middle stump through the covers for four.</p>
<p>Finn's first 8 matches were played for all three clubs that JWXI has represented; a happier record that is unlikely to be surpassed.</p>
