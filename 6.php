<p>The god of gods, Warren Sidebottom is the closest thing to a real life Midas - even before he puts on his baggy red cap.</p>
<p>Regardless of how his Johnnie Walker XI career turns out, Sidebottom will go down in folklore as the original feared leader, leading the troops in both the first ever match and the foundation of the club itself. On the field he displayed a remarkable ability to take control of a large group of rowdy men whose minds weren't always on the match at hand, while allowing plenty of fun at the same time. He also had the grace to give everyone a fair go, regardless of injury or retardation, often with startlingy good results. A sometime off-spinner and at his best a tremendous keeper, he even managed to use his nose for a hunch on himself on one occasion, when he shocked Old Students with a stunning spell of new ball bowling to snare 4/19 off eight overs. But it is with the bat that he makes the biggest impact, having played some scintillating innings over time, with consistency his only downfall.</p>
<p>Sidebottom's greatest innings is undoubtedly his 89 against Invergordon, when he tore the opposition to shreds. Had he not been given leg before by his father Greg (and it was out), he would surely have set a high score mark that would take some beating; when he was dismissed, little more than half the 40 overs had been bowled.</p>
<p>Following that first season, Sidebottom made three appearances in Melbourne and although he didn't taste victory, his fast starts provided a platform that so easily could have brought about a win. He finally made a proper return to the team during Willowfest 2010, where he excelled in the field and, aside from the Grand Final, appeared to be more dependable with the bat than ever before. If he can extend his time at the crease at the 2011 carnival, the Johnnies could be unbeatable.</p>
