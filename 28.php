<p>After a slow start to his career, Clinton Murdoch eventually blossomed into the spearhead that has dominated non-Johnnie Walker XI cricket for years.</p>
<p>Expectations were high going into the first Melbourne season when Murdoch announced his availability, having starred in B Grade while the rest of the boys created the Johnnie tradition. The only problem was that he hadn't played for 18 months; as a consequence, he managed just one wicket in his first five games. Indeed, the highlight of this period was his lance run out on debut, when he beat the batsman in a footrace from mid-on and celebrated with a soccer slide. He found some sort of a groove as Christmas approached, but a return of seven wickets @ 44 was an unhappy outcome. Fortunately, Murdoch's batting improved as the season went on, after a return of three ducks and a single during his barren bowling spell. A deal had been struck so that he could bat at six if Vaughan Garner took the captaincy, and while that fell through after consecutive blobs, a semi-successful chance to open gave him confidence to convert further middle order opportunities into significant contributions. Twice half centuries went begging; once a by his own hand and again when some big hitting from Andy Taylor hogged the strike and a misfield for four on the second last ball left him stranded two runs short.<p>
<p>The 2009/10 season finally saw the real Murdoch bare his teeth, propelling the Johnnies into the finals for the first time with 17 wickets. Once more the runs came late, however he made up for it with an aggressive 59 in the last round. His fielding - often lambasted - also improved substantially, with seven catches and a part in three run outs.</p>
<p>There have been no doubts on Murdoch's form at Willowfest; two economical performances bookended a sensational 12 wickets at the 2010 carnival. That effort was enough to earn a start in the team of the carnival, while pushing the team into a maiden grand final appearance. Amongst the haul was a spell of 4/29 against rivals Hutt River, including an opening burst of 4/9 off seven overs. Despite this, a convoluted (read unknown) voting system robbed him of the Champion Player award, losing out to the all round performances of Josh O'Dwyer. His bowling will be needed more than ever for the premiership defence, in which he will become only the fourth player to bring up 47 matches.</p> 
