<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Round 11 v Sunshine YCW</u></b></h3>  
<br></br>  
<p>The boys were keen to atone for their previous showing against Sunshine YCW, when eight batsmen made 16 runs combined, however Chris Taylor and Vaughan Garner's need to leave early necessitated bowling first on winning the toss. Ben Carr created the early pressure, proving too quick for the hapless Wijeyasiri, en route to an opening spell of 1/6 off five overs, including an starting run of 22 consecutive dot balls. Steve Ritchie also impressed early, bowling Williams off the foot for his maiden JWXI wicket, but an expensive final over allowed Sunshine to break the shackles, racing to 2/76 at drinks.</p>
<p>After going for some stick himself early, Geoff Cox persisted and gained the wicket of Bulzomi when Kale Cetinich seemingly misjudged a skier at long on before throwing himself backwards and somehow holding on with his right hand. That act seemed to have sucked the fielding skill out of the rest of the team next over, when Cooper Reid created three chances from the same batsman; an bottom edge that ricocheted off Garner's hip to Mick Pataki, who couldn't cling on with his right hand after going left, a skied sitter that Reid himself dropped, and an even easier chance to long on that Ritchie put down. Steve Pataki added to his growing fielding reputation by showing the rest of the boys how it was done to give Cox a second wicket, before Reid picked up his own brace within three balls after Garner affected consecutive stumpings. Carr's final over was taken for 11 - including a staggering square cut for six by Evans - but a wicket off the last ball of the innings gave him the top figures of 2/25.</p>
<p>Taylor and Garner arrived at the crease with an already spread field greeting them, which Taylor would spread even further by helping take Bulzomi's opening over for 15. A potential classic was eventually ended by his own hand, walking for 38 after doubt as to whether he had hit the ball or the pitch. Garner survived a chance when Lawler spilt a leg side offering, but impatience going for quick runs proved his downfall when he offered a simple chance to deep mid wicket. Another partnership was crucial to put the chase back on track, which was provided by Mick Pataki - playing the anchor yet again - and the aggressive Carr, who became the third victim out of three for the impressive young left armer Dial. Jacob Cox could only manage a second ball duck, plummeting his career strike rate to 276.92, while Cetinich's poor form continued, making just 7.</p>
<p>That dismissal brought the two old Glenroy stagers in Pataki and Geoff Cox together, with 70 still required off the final 10 overs. Pataki abandoned his safety first style and proceeded to match Cox stroke for stroke, reaching 50 off 63 deliveries. Cox's cameo ended on 27, bringing to a close a stand of 53 in just under seven overs. Pataki would not be moved however, bringing up the winning runs with consecutive boundaries in the 33rd over to finish on a JWXI season high 72. Steve Potger provided the finishing touches by picking up a boundary to ensure the result was correct.</p>

