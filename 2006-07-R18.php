<h3 style=”text-align:left”><b><u>2006/07 - Round 18 v Old Students</u></b></h3>    
<br></br>
<p>The anomaly of the draw had the JWXI facing ladder leaders Old Students once more in the final game of the season. The match had not even begun when controversy engulfed the team. Several crowd members had brought along copious amounts of alcohol to celebrate the final match – a point apparently already known by club management. Vaughan Garner was banned on the spot after being caught with a Carlton Cold in his hand, despite rules stating alcohol can be consumed before the match, while Warren Sidebottom would be cautioned following the game, after his drinking and attempt to walk out to bat in a Guinness jester hat apparently brought the club into disrepute. Garner would later field in the same match, while both players would be welcomed back to the club with open arms almost immediately. The match itself was rather uneventful, although the batsmen gave a much better account of themselves than the previous week. Simon O’Shea top scored with 57 in a total of 8/157 and, despite Chris Taylor’s 27, managed to pip Taylor in the season runs tally by just 2 runs. In reply, Old Students closed their innings at 5/178 after just 26 overs, having passed the target 4 wickets down. Doug Taylor used the casual conditions to his advantage to pick up bizarre figures of 3/15 off 2 overs.</p>
