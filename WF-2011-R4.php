<h3 style=”text-align:left”><b><u>Willowfest 2011 - Round 4 v Ray White's Dirty Dozen</u></b></h3>    
<br></br>
<p>Round 4 saw us come up against the unbeaten Dirty Dozen, who had enjoyed comfortable wins over both SPL teams and reigning premiers Kangarilla B. As a result, any hope of taking out the title hinged on a positive result. The opposition got off to a solid start, reaching 61 without loss before Simon O'Shea, bowling at second change and in the middle of an uncharacteristically expensive spell, caught Oliver Braithwaite in front. Josh O'Dwyer was then rewarded for applying the brakes on the innings, following up consecutive maidens with the important wicket of top scorer Curran, courtesy of Andrew Dowell's first gloveless catch in the baggy red. Perhaps the most important spell was delivered by the unlikely source of Kale Cetinich, who was able to extract disconcerting bounce and lateral movement from a helpful pitch. His first wicket came in his second over when he bowled Andriskie through the gate, and another in his last over rounded out tidy figures of 10-2-2-32. At the other end, Michael Dann extended his bowling purple patch with 1/27 off 9, including a teasing floater to Kerr that allowed Vaughan Garner to complete an easy stumping. It was a welcome change for players and umpires alike that Garner had removed the bails at an acceptable time, as the innings was littered with several occasions where the wickets were needlessly disturbed; a notable example occurring when the wicketkeeper mangaged to knock a stump clean out of the ground despite not attempting to make contact. O'Dwyer and Matt Barclay routed the tail with their second spells, as the last six wickets fell for just 23.</p>
<p>Warren Sidebottom and O'Shea responded with a sound start, cruising to 65 in the 17th over. It was a cruise in more ways than one, as the pair regularly turned down relatively comfortable threes in the sweltering heat in favour of even more comfortable jogged twos. Sidebottom was the first to go for 30 (including 11 2's) after calling O'Shea for a single to mid on, only to be turned back half way up the pitch after a fine cutoff. When O'Shea departed two overs later, the Johnnies still required 88 for victory, but Barclay and Dann put any doubt over the result to rest with an excellent display of controlled hitting, bringing up the winning runs with almost 15 overs to spare. Barclay finally had some Willowfest luck on his side, being dropped twice, as he returned to form with a lovely unbeaten 49, while Dann extended his third start of the carnival to 33 as he rotated the strike with ease. To top it off, the duo took the third wicket record partnership with them, setting a new benchmark of 89*, while they also increased the Willowfest best mark for the second day in succession.</p>


