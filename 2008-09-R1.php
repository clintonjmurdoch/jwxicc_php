<h3 style=”text-align:left”><b><u>2008/09 - Round 1 v Moonee Valley</u></b></h3>    
<br></br>
<p>The first game of the new season saw fresh faces light up the reformed team (along with a few fill ins). Fears of fielding a depleted side grew as Chook Williams, Cade Ringin and Rory Herd disappeared – their arrival with eight minutes to game time eased concerns. Captain Vaughan Garner lost his maiden toss as Moonee Valley chose to bat first on the large but fast JP Fawkner Reserve. Things started brightly when, after a tight first over from Clinton Murdoch (6-3-0-18), Williams came close to striking with his very first ball (bump ball to gully), before inducing an edge to Mick Pataki at slip with his second. Herd’s maiden delivery also almost struck – youngster Kenny Jones-Oliver dropped a tough chance at mid-on – before he too poached a wicket in his first over, as Williams took 4 juggles to control the ball. 2/30 should have become 3/30 as Valley captain Jake Little was given an enormous let-off on nought, but Andrew Taylor spilt a sitter at cover. Little went on to post a quick-fire 61 along with several more missed chances, before Garner (2-0-1-11) brought himself on to remove his opposite number.</p>
<p>With the best batsman back in the sheds with the score on 4/125, Moonee Valley lost 6/23 as a result of effective second spells from Williams and Herd and a lot of Piccione. Ringin decided a simple underarm to Williams from mid-off, with the batsman stranded a quarter of the way up the pitch, was no match for a bullet throw piercing middle stump, while Murdoch chose not to chance his arm at all, beating his victim in a footrace to the line. The soccer-sliding celebration that followed has probably been rarely viewed in such a sport, on such a terrible ground, or with the score reading 9/148 in the first innings. Williams led the bowlers with expensive yet penetrative figures of 7-1-4-43, but was ably supported by the economical Herd (7-2-1-14) and Ringin (6-1-1-19). A final score of 9/151 did not do justice to the pressure the bowlers placed on the batsmen, yet dropped catches (in no particular order: A. Taylor, Pataki, Shannon Ryan, Jones-Oliver, Herd and almost Williams) allowed the score to increase freely.</p>
<p>The reply was swift from the outset, as Jarryd Mills and Pataki took the score to 23 in 3.4 overs. Pataki’s departure for 18 off 13 balls saw Garner join Mills at the crease, yet their long-awaited maiden partnership lasted just two balls; Mills (4) succumbing to a mistimed pull shot after claiming correctly that he couldn’t play the shot whilst wearing no helmet. Solution? Don’t play the pull shot, or harden up. Chris Taylor’s entry at number four put the chase firmly back on track. His fierce offensive style in combination with his total lack of a defensive stroke, on a bad day, can make him look silly, but he showed up on song, belting the Moonee Valley attack to the tune of 34 of 31 balls in a third wicket partnership of 54 with Garner. This included a swat off his chest that took off as if hit by a tennis racquet, sailing over square leg for a maximum that almost went through his own car windscreen. At drinks, the Johnnies still required 68 runs from 18 overs with seven wickets in hand – a seemingly comfortable position, with Garner still at the crease unbeaten on 22. Following the drinks break however, the middle order cracked – A.Taylor (3), Murdoch (0) and Ringin (0) all departed within six balls after being trapped on the crease by unknown off-spinner John O’Brien (then again, all players are unknown to the JWXI at this point in time), to send the score crashing to 6/90. Panic appeared to have set in, however Garner and Herd had other ideas, seeing the team home on their own with four wickets to spare. Garner was particularly impressive, notching up his maiden fifty in his first game as captain; eventually top scoring with an unbeaten 64 off 80 balls, including 11 boundaries. The result may have been different had Moonee Valley not missed a tough chance offered by Garner when he was on just 5. Herd provided excellent support in the 78 run stand, scoring 29 off 36 balls (including two sixes), but undid all his good work by getting out on the final ball of the innings.
</p>

