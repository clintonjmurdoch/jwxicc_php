<h3 style=”text-align:left”><b><u>2006/07 - Round 15 v Kialla</u></b></h3>    
<br></br>
<p>While the final margin might suggest otherwise, this was a tight contest for the entire match. Things did not start well for the JWXI as the innings slumped to 78 all out in 29 overs. ‘Stand in captain’ Patrick Lahn did not live up to the hype, making just 2, as Simon O’Shea, Ashley Keating and Jeremy Watt were the only batsman to reach double figures. The bowlers had a much better day, almost winning the match for the JWXI, as Kialla were reduced to 5/62 with less than 10 overs to spare. The final score of 6/103 showed what might have been had a competitive target been set, although Kialla were holding their best batsmen back in case of emergency. Terrific spells of bowling from all concerned; Chris Taylor (8-1-26-3), Vaughan Garner (8-4-11-2), O’Shea (8-3-11-1) and Andrew Watt (8-2-10-0) put the stranglehold on the Kialla innings. This was evident in the innings of a young Kialla opener, who carried his bat for an astounding 33. </p>
