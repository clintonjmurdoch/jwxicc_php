<h3 style=”text-align:left”><b><u>2006/07 - Round 16 v Mooroopna</u></b></h3>    
<br></br>
<p>A third heartbreaking loss against Mooroopna, as dropped catches from a number of players and soft dismissals resulted in a margin which did not signify the closeness of the match. Mooroopna frustrated the bowlers with a fourth wicket stand of 120, to take them to 7/217. Chris Taylor snared 2 wickets, while Vaughan Garner (1/31) was unlucky all day, having the opposition captain dropped on a low score, before creating a direct hit to run him out on 78. Of course, Garner couldn’t complain, dropping 2 catches himself, however teammates blamed it on his recent hand injury – an excuse he could easily live with. In reply, JWXI was dismissed for 126 after going to tea within range of the target. Garner (30) was dismissed by a freak catch at short leg off a full blooded sweep in an attempt to up the run rate, while Simon O’Shea and Jeremy Watt (both 21) contributed well. </p>
