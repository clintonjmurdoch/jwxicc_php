<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Round 7 v Barkly St. UCCC</u></b></h3>  
<br></br>  
<p>Following three weeks of walkovers and rain, most of the boys were just glad to get on the park. Most boys did not include Chris Taylor, who sensed a weak opposition and put them in at the toss, hoping for a quick kill. It seemed to be an ill informed decision early as Barkly St raced to 26 without loss in the fourth over. The first breakthrough came when Vaughan Garner alerted Taylor to a distinctly bare leg side field for Bushells Jayakodi's inswing. Taylor's subsequent placement of a square leg paid immediate dividends as Barkly St skipper David Nguyen clipped a leg glance straight to Kale Cetinich, who took a good low catch. Three overs later Taylor came up with the big scalp of Jason Tran (33 off 27) who seemingly edged behind to Garner, although it quickly became clear that he had hit his pad. When Raymond Kuffer popped a slower ball off Chris to Andy Taylor a short time later, the Streeters' innings had quickly entered freefall mode. This meant little to Ashish Narula, who attempted to charge Ben Carr on three consecutive balls, for a miss, an edge to gully and a flying edge that eluded a leaping Chris Taylor's grasp at first slip, who almost managed to parry it for six. As Barkly St were just starting to regain control, Cooper Reid was given an over before the drinks break and struck with his second ball, with young debutant Kevin Spaull taking a good catch off a shot many times his own height. The catch gave Spaull plenty of confidence, who would later effectively tell Andy Taylor to piss off as he made another catch and told Chris Taylor to stop thinking about his girlfriend while Taylor lumbered after a ball (Garner was quick to inform him that he didn't have one!).</p>
<p>Shortly after drinks, Reid finally sent Narula on his way in peculiar fashion when, having stepped back to clip a short ball to fine leg, the batsman took another fateful step back and dislodged his off bail. Despite not wanting to claim the wicket, Reid was spurred onto greater heights; even managing to conjure a sitter that Steve Pataki could cling onto at mid-on. Some late hitting threatened to dent his figures, but 4/40 off seven overs was just reward for some quality spin bowling. That late hitting dented other figures however and, aided by some poor fielding and a good karma but otherwise poor decision to deny Jayakodi a third wicket and Garner a third catch, added an unbeaten 61 in the final nine overs.</p>
<p>The chase got off to a bad start, with openers Mick Pataki and Ben Carr sent back inside the first five overs. With Spaull for company, Garner dropped anchor and encouraged the young batsman to play his shots. Despite only managing 14, Spaull's innings gave the innings the impetus it required, however his dismissal served to bring about a typical JWXI collapse, as both Cetinich and Billy Morrison failed to decipher the baseball style bowling action of Ted Hayes. Stuttering at 5/45 halfway through the chase, Chris Taylor came in and duly whacked his second ball over mid-wicket for six. He enjoyed it so much that he repeated it another four times, and on each occasion Hayes would saunter over and give him a high five. When Taylor connected with a cover drive and a late cut, it appeared that he was set for a big innings, but a tame block back to the bowler ended a staggering innings of 49 off 23 balls. His dismissal meant that 71 more runs were required off 72 balls with four wickets in hand, but three wickets in the next four overs - including Garner for a patient 30 - effectively ended the chase. Matthew Cross and Jayakodi gave the team hope by adding 29 in 39 balls, but in the end there was too much to do.</p>

