<h3 style=”text-align:left”><b><u>2006/07 - Round 1 v Pine Lodge</u></b></h3>    
<br></br>
<p>The first game for the new club would paint a good picture of just what the fledgling side could achieve. Expectations were low for the season, however on a good day anything could happen. An inspired performance from the inexperienced bowling attack saw Pine Lodge dismissed for a paltry 97. Tennis player-turn-cricketer Chris Taylor impressed with a spell of 2/15, while Simon O’Shea dismantled the middle order with figures of 3/23 from 6 overs. The run chase proved labourious; with the win eventually coming with just 3 wickets in hand. Dale McNair top scored with 27, while Steve Tate, James Hainsworth and Vaughan Garner all passed 20. Garner and Chris Murphy entertained the crowd late in the day with a thrilling 31 runs put on for the final wicket. In a sign that the Johnnie Walker XI thrives in no pressure situations, the last three wickets put on 72 runs once the target had been achieved.

