<p>If ever one were to pick a player that best represented the schizophrenic nature of Johnnie Walker XI, anyone with the last name Taylor instantly comes to mind, however Andrew Taylor's Jekyll and Hyde performances in all cricketing aspects have him as the frontrunner. Starting as he meant to go on, Taylor's slinging, almost roundarm action netted him seven wickets in his debut season - including three separate two wicket hauls for a combined 27 runs) - and over the following seasons established a feast or famine (cheap 2-fer or hacked) pattern in his bowling. Out of 12 wickets, he has 5 two wicket innings - of which the most expensive is 2/12 - and when he hasn't taken a wicket, his economy rate balloons to 8.52. A bad back has limited his spells in recent years, and it is testament to his partnership breaking ability that he is still sought after for a trundle when times are tough.</p>
<p>Taylor's batting style can be best described as a combination of indoor cricket and digging. A typical innings includes kamikaze running and several paddled boundaries behind square leg. Both these traits were on show in his second match; with Vaughan Garner, he put on a quickfire unbeaten 35 for the ninth wicket to close the innings. Taylor's contribution was 21, while he also earnt the side 5 leg byes by forcing his partner through for a ball that had trickled off the thigh to the fielder at silly mid on, whose subsequent throw to run Garner out went for four. Like his bowling, Taylor's batting has been restricted to cameos, as a highest score of just 24 attests, but he never wavers from his role as a quick scorer.</p>
<p>Taylor's fielding is absolutely electric, unless the challenge at hand is an easy one, where he occasionally gets ahead of himself. His list of stunning catches - one or two handed - is a lengthy one, making it difficult at times to remember a specific take.</p>
