<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Round 8 v Sunshine YCW</u></b></h3>  
<br></br>  
<p>Half an hour before the start it appeared that we wouldn't be able to field a full side. Fifteen minutes before the start we had 12 players - the whims and ways of low grade one day cricket. Fortunately we called the twelfth man in, as he would play a crucial part in the nailbiting victory.</p>
<p>In a surprise move, Chris Taylor won the toss and uncharacteristically decided to bat first - perhaps finally put off by the previous week's events. Mick Pataki adopted a similar attitude to ensure he didn't meet the same fate, although he pushed the point a little by facing 30 deliveries to Ben Carr's 6 at the same stage. Facing barely a ball an over wasn't to Carr's liking and a full inswinger was enough to breach his defence and consign him to a third failure in as many games. This brought Vaughan Garner to the crease, who chose Pataki's method of patience and allowed Sunshine to do most of the scoring. Pataki (32) fell on the edge of drinks to leave the Johnnies in an encouraging position of 2/60.</p>
<p>Garner decided his ailing bat needed replacing, borrowing Taylor's equally ailing but more powerful SS, and, combined with  a change to some of the more deplorable bowlers ever witnessed in the opposition, lifted his scoring rate dramatically. On two occasions against Andrew Bilson he would run out to a ball bouncing twice off the pitch, hitting one away for two and another half a metre away with a shot more at home on a croquet pitch. Kale Cetinich preferred to indulge in larger hitting, but on 5 could only manage to sky one to long on, who completed the catch after a volleyball set to himself. Debutant Satish Subramanian chose a completely alternative method, paddling balls around the corner or setting off for suicidal singles. Eventually he was caught off a tame chip to mid wicket, giving Bilson the only two wickets of his 79-match career (thanks MyCricket).</p>
<p>After bowling a few balls off the pitch himself, youngster Patrick Wilcox quickly found that, as long as the ball is on the pegs, you're always in with a chance - even at 30km/hr. Aaron Pataki succumbed to this, playing far too early and losing his off stump. The following ball Chris Taylor couldn't clear deep mid on, putting his mate Garner on a hat trick ball for the second time in his life. Garner chose to respect a slow half tracker and avoid the jeers his skipper had just received. Andy Taylor took his partner's advice to watch the slow pace a little too literally, playing the latest of cuts to a ball that had already clipped the top of middle, to give Wilcox a stunning three wickets. Garner's increasing aggressiveness got the better of him on 33, leaving Clinton Murdoch to steer the ship with just Patrick Lahn and Steve Pataki for company. Lahn made a solitary single before edging behind, while Pataki somehow edged over cover to score his maiden JWXI run in his sixth match before being dismissed shortly after. While 112 would be a tough chase on the lushest of lush fields, it was a crying shame that, excluding M.Pataki, Garner and the unbeaten Murdoch, the remaining eight batsmen could manage just 15 between them.</p>
<p>It took just over 12 overs to make the first breakthrough when Chris Taylor's slower ball rattled the stumps. At 1/47 however, Sunshine still looked comfortable, but Aaron Pataki was keen to make amends for his failure with the bat, turning in a tremendous spell of off spin bowling in Cooper Reid's absence. He picked up two wickets leg before and two caught in the deep, finishing with 4/24 off seven overs. In between, Garner held onto a good catch standing up to Mick Pataki, seemingly convincing the umpire of an edge by running off like a lunatic, and Ben Carr finally took his well deserved first wicket of the season; ironically with a horror half tracker that was cut to Chris Taylor at point. Another wicket to Carr left the score on 8/97 - a collapse of 6/50. Carr should have wrapped things up after Garner snaffled another edge, however this time it was not detected, prompting Mick Pataki to take crude exception. Luckily, Chris Taylor and Murdoch were quickly on hand to settle the waters and avert World War III. Pataki himself got revenge, bowling the same batsman next over.</p>

