<h3 style=”text-align:left”><b><u>Willowfest 2010 - Grand Final v Kangarilla B</u></b></h3>    
<p>For the first time in the club's history, the Johnnie Walker XI were contesting a Grand Final, however just like the previous year, a tired squad assembled at City Turf #3. Confidence was high though, after a comprehensive win over opponents Kangarilla B the previous day, although the task was going to be tougher second time around with a number of key players returning for the South Australian side.</p>
<p>Having won the toss, Kangarilla made a slow start, losing Gordon early to a good low catch by Pete Barclay off Clinton Murdoch's bowling. Josh O'Dwyer had Gambell leg before soon after to leave Kangarilla struggling at 2/17 in the ninth over. The toll of four straight days of cricket began to show its toll as Michael Ross and Rick Sullivan put on a largely pressure-free 51 runs. Simon O'Shea at least ensured that run scoring would remain difficult, sending down 29 consecutive dot balls before finally conceding his first run. Chris Barclay eventually broke the stand when Ross sent a high ball down Murdoch's throat. Murdoch quickly added another wicket to resume the ascendancy, however a 32 run partnership between Sullivan and the previous day's enemy Josh Ross further frustrated the Johnnies. Sullivan's dismissal - to an excellent Matt Barclay return catch - was the beginning of the end for Kangarilla, who lost their last six wickets for just 28 in 7.1 overs. In spite of the physical struggle, the bowlers did a terrific job; not one of them conceded more than four per over.</p>
<p>Chasing just 129 for a maiden premiership, Matt Barclay and O'Shea started sensibly, taking the score to 17 in the 7th over, before O'Shea edged behind. Michael Dann could only face six balls until he received a tough lbw decision; at 2/32 panic shouldn't have set in, but that's exactly what happened. A succession of big shots resulted in a collapse of 3/6 in 19 balls, including the big scalp of the well set skipper Barclay for 16. In form batsman O'Dwyer was still at the crease however, and with Vaughan Garner for company, he attempted to put the chase back on course. He had reached a quickfire 19 before he fell to another big shot against chief destroyer Gambell. Garner attempted his own big shot next over, only to edge low to second slip; hanging around to confirm that the catch had carried nearly inciting a riot in the over exuberant cordon. Pete Barclay didn't last long, allowing Murdoch and Colin Finn to delay the inevitable by adding an exciting five runs in 34 balls.</p>

