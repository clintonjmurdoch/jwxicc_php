<h3 style=”text-align:left”><b><u>2006/07 - Round 11 v Toolamba</u></b></h3>    
<br></br>
<p>An historic day for the JWXI, as Chris Taylor became the first player (and possibly the first ever non-cricketer) to make a century. It wasn’t official until after the innings had been completed, as the scorebook showed varying scores from 98 to 102, but the score now shows an innings of 101 not out, including 12 fours and 4 sixes. The highlight from a team perspective was a fourth-wicket stand of 127 between Taylor and Simon O’Shea (45), while Ashley Keating (48) and debutant Darren Meek (32) added 63 for the second wicket to push the score to a season high 5/276. The result was never in doubt, however the JWXI were disappointed to allow Toolamba to reach 9/181. O’Shea led the bowling with 7-1-22-2. </p>
