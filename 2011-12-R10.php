<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Round 10 v West Coburg</u></b></h3>  
<br></br>  
<p>Strong winds and a fast outfield set up a tantilising top of the table clash with West Coburg. Mick Pataki and Ben Carr got off to a solid start against a barrage of genuine fast bowling, taking the score to 18 after six overs. Carr in particular thrived early, flicking Nathan Ward off his pads for six and lofting Pas Triulcio over his head for a boundary. Pataki was struggling to get bat on ball against Triulcio, and it was no surprise when he edged behind for a laborious 17 ball 1. Kale Cetinich came and went after being beaten for pace, leaving Vaughan Garner to keep out the hat trick ball, which he did by blocking it in to the pitch and having it bounce back into his face. With survival being paramount, Carr’s good form became crucial to the team’s hopes, as he blocked out the final testing over of Triulcio’s first spell, before tucking into the slower bowlers. When dismissed, his 46 represented over 75% of the total, with Garner’s 4 behind nine extras as the next highest score. Aaron Pataki mirrored his debut effort by being bowled third ball for 1, making way for Chris Taylor, who typically smacked his first ball away for four, then later mishit a ball against the breeze that somehow carried for six, matching a similar effort from Carr with the wind. After surviving an easy caught behind chance, Taylor offered a second serving and was sent on his way for 17.</p>
<p>Cooper Reid arrived with the mantra of playing straight echoing in his head and he listened for his first ball boundary, while his second ball was a textbook pull over midwicket. The pattern continued as he picked the right shots for the right lengths while sticking to his natural game. The result was a personal best 31 – off just 19 balls – which would set up the final charge. The free hitting at the other end finally rubbed off on Garner, who managed to connect with a straight drive at the umpteenth attempt, punching it through long on for a bizarre low six – his first in two years and only his fourth ever. He enjoyed it so much that he had another go five balls later, hooking the off spinner Surendrakumar high over backward square leg. The highlight of Andy Taylor’s brief stay was helping to take nine off the frugal Triulcio (who at one stage had 2/5 off five overs); first blinding the keeper’s view of a leg side wide, then hanging his bat out at a wide one and poking it over slip’s head. Billy Morrison picked up his own personal best, before Matthew Cross took charge in the final three overs with a brazen 25 off 15 balls, to boost the score to 192. Meanwhile, Garner’s steady acceleration took him to 42 – four short of 1000 career runs – and lifted his season average to a staggering 86.50 from five innings.</p>
<p>Carr and Garner began proceedings with the ball in similar fashion to their partnership with the bat; Carr’s pace offset by Garner’s swing. Carr gained the reward by having Cranage caught slashing to third man, which brought the dangerous Triulcio to the crease. He took a four and a six off consecutive balls in Garner’s fourth over and another boundary in Carr's fifth over to force a double change. Kale Cetinich settled into a tight spell, however Chris Taylor couldn't replicate it at the other end and was hit around at will, regardless of the quality of ball. Triulcio and Kirby's partnership had almost taken the game from the Johnnies' grasp when Carr returned to remove Kirby with another short ball - this time cut to cover - and complete a valuable maiden. The turning point of the match, however, came with Andy Taylor's sensational direct hit to remove Triulcio. What appeared to be a comfortable two to deep mid wicket became the narrowest of run outs when Taylor swooped and delivered a perfect bullet throw. Mick Pataki then bowled Ward off his legs, and tight overs from himself and Cooper Reid seemed to have finished the match, however Jake Drummond was having none of it, taking Reid for three consecutive sixes to bring the equation down to 27 off 18. Pataki would allow just 10 off his final two overs and Cetinich 10 off his solitary over to confirm a seven run win, ended fittingly with an Andy Taylor catch in the deep.</p> 

