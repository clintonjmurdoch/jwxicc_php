<br></br>
<p>The new team was quickly brought back to earth in the second game of the season. A complete inability to defend the ball led to an exciting yet all too brief innings of 124 in just over 25 overs. Chris Taylor top scored with 25, with good contributions from Simon O’Shea and Greg Sidebottom. In reply, Toolamba smashed 201 runs in their 40 overs. O’Shea continued his fine form with 8-1-20-3, while Sidebottom took 2/18 and unlikely star Patrick Lahn picked up 2/24 from his 7 overs.</p>
