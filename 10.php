<p>Doug Taylor's main legacy will most likely remain his ability to bear three boys who would each put their own stamp on the JWXI, however his own contributions to the team's formative year will not be forgotten quickly. 
A dour opening batsman by trade, Taylor struggled for runs, resulting in his talents being utilised in the field, with slightly more success. Brought into the attack as an afterthought as the inaugural season's final match had already been decided, Taylor took advantage of the casual conditions to pick up his only three wickets in twelve balls. His other highlight - to all but himself - occurred when, after having his thumb broken while batting, he managed to get it whacked again fielding at slip in the same match.</p>
