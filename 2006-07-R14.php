<h3 style=”text-align:left”><b><u>2006/07 - Round 14 v Invergordon</u></b></h3>    
<br></br>
<p>An impressive all round display resulted in a convincing victory – the sixth of the season. Consistent bowling saw Invergorden dismissed for 162 – a low score for such a fast outfield. Chris Taylor (3/40), Andrew Taylor (2/11) and Vaughan Garner (2/31) all had good days with the ball, however the fun was still to come. Captain Warren Sidebottom had had a lean year preceding this match, however he came good when it counted, smashing his highest score of 89, before being given out plumb LBW by father Greg. Partnerships of 75 with Ashley Keating (35) and 50 with Garner (43) saw the JWXI post an impressive 235 all out from 39.5 overs. The score would have been 9 down at the conclusion of the match had Garner not run himself out on the penultimate ball of the day chasing the still-elusive 47. </p>
