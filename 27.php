<p>Perhaps the most significant contributor of the second wave of Johnnie Walker XI representatives, Jarryd Mills can rightly lay claim to belonging to the top echelon of players to have donned the baggy red. Recruited from Flemington, Mills was selected as a wicketkeeper-batsman to prop up a team full of bowling all-rounders, and although he would have success with the ball, it was in his role that he would quickly prove his worth to the team.</p>
<p>Following an inauspicious debut, Mills brought up his maiden fifty at the second time of asking, guiding a mammoth chase of 229 against Pascoe Vale SC. He even took the unheralded Ben Perry along for the ride, putting on 110 runs for the second wicket - a record that stands to this day. Consistent contributions were punctuated by an end of season run of 66, 45 and 55, resulting in an all-time high season tally of 386 runs and a comfortable victory in the Club Champion Award. This wasn't enough to lift the side off the bottom rungs of the ladder, however the team position meant that he was often called upon to roll the arm over, with the results good enough for equal third on the bowling list.</p>
<p>Mills wrote his name into the record books the following season by smacking 102 against Royal Park Reds; the highest score at the time and the second century in club history. His hundred was almost guaranteed when, in the 60's, he hooked a half-tracker straight to square leg, who shelled the tough chance before running out Dolf Reid, who had decided on a non-existent single. This innings was followed by a blistering 48 against Barkly St. UCCC, however the hope of a watershed year was dashed with a run of just one 30-plus score in the next eight innings. Thankfully, he faced Royal Park again a week before finals and, having played himself back into form, struck a gorgeous run a ball 52 in the qualifying final against The Socials. Chasing 227 for an unlikely win, Mills was dismissed in the first over (18th overall) after drinks, having put on a chanceless 101 with Vaughan Garner, but the chase would ultimately fall 28 short. Despite his barren mid-season run, he still managed 369 runs and, courtesy of another all time record of 19 wicketkeeper catches, a second Club Champion Award.</p>
<p>With the lure of higher level cricket awaiting him, Mills managed just three appearances in 2010/11, captaining the side once following Chris Taylor's departure and picking up another fifty in his final match. His next appearance in the baggy red is unknown, due to his commitments to rival club Hutt River and the uncertainty of JWXI's Melbourne future, however his legacy is secure, even if it is for the stumping curse that has afflicted him to the point where square leg umpires regularly refuse stone dead appeals.</p>
