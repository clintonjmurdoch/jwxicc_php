<h3 style=”text-align:left”><b><u>2006/07 - Round 5 v Invergordon</u></b></h3>    
<br></br>
<p>A return to form by the bowling attack led to a hard fought, second victory of the season. A tough display batting first led to a competitive score of 8/152, with Greg Sidebottom, Dale McNair and Andrew Taylor all making solid contributions. Taylor and Vaughan Garner in particular entertained the crowd with an unbroken ninth wicket stand of 35 (around 25 of which was composed of leg byes, byes and indoor cricket-style running between wickets), including the unusual feat of earning 5 leg byes following a pad down to short leg and a desperate dash to make ground. In reply, Invergorden could only muster up 113 in 27 overs. Simon O’Shea again led the way with 3/26, Andrew Watt got 3/34 and Andrew Taylor 2/4.</p>
