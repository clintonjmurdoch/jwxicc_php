<p>Ben Perry's only match will forever be remembered by all who witnessed it for a remarkable innings against a high quality opponent. Citing a golden duck as his entire career and possessing little in the way of sporting skill, Perry edged the Pascoe Vale attack to distraction en route to 44 - the highest score on debut at the time. Playing the same hack drive repeatedly, six boundaries were milked over, under and through the packed slips cordon. To rub salt into the wound, Pascoe Vale's A-Grade bowler finally managed to put one on the stumps, only for Perry to smash the ball straight back past him for another four. So emphatic was his innings that his dropped dolly earlier in the day had been completely forgotten. As well as helping Jarryd Mills put on 110 - a record for the second wicket - Perry's innings was crucial to JWXI's record chase of 229. His reward? Being doused in unwanted light beer. In a sad footnote however, Perry was found to have played in the competition for Sunshine YCW the previous year and had not been cleared, resulting in one of JWXI's greatest victories being officially recorded as a loss.</p>
