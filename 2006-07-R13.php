<h3 style=”text-align:left”><b><u>2006/07 - Round 13 v Kialla</u></b></h3>    
<br></br>
<p>An old-fashioned flogging as Kialla belted the JWXI attack into submission, scoring 8/308. Vaughan Garner, Andrew Watt and Chris Taylor all took 2 wickets. The only thing left to do was to make an audacious play for the target – after 2 overs, the score was 1/22 – but a consistent flow of wickets led to a final score of 123 off 21 overs. Garner capped off a good return to the batting line-up following his wrist injury to post 30 at the top of the order in his first innings wearing the baggy red, while Darren Meek, Chris Murphy and Andrew Taylor all passed 20.</p>
