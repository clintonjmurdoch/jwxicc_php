<h3 style=”text-align:left”><b><u>Willowfest 2011 - Round 3 v SPL St Columbs</u></b></h3>    
<br></br>
<p>Match 3 of the carnival saw the return of Ben Mason for his first capped appearance in two years (and just his second overall). Needing a win to keep in touch with the tournament frontrunners, Matt Barclay found the perfect time to deliver his devilish best, as he removed the openers with just 12 on the board, both of whom had inexplicably lobbed catches down Simon O'Shea's throat at mid off. O'Shea's flashy new red pants were proving to be a ball magnet after Josh O'Dwyer enticed another shot in his direction. A thirty run stand punctuated by bizarre tippity style running ("easy two, easy two!") threatened a fightback, but O'Dwyer decided the time was right for an unassisted wicket, trapping Hafeez leg before. Barclay struck immediately upon his return, with another two wicket spell giving him career best figures of 4/21, while Michael Dann continued his wicket spree by cleaning up three tailend wickets for just 11 runs. Andrew Dowell even found time to impress with some swing bowling - a far cry from his 1/27, 1.4 over and out performance the year before. A collapse of 7/21 was completed when Rhys Collier and Vaughan Garner combined for a run out to end the innings; a particular relief for both - Collier's own, tighter set of red pants had taken the catch tally to six but resulted in him battling severe chaffing (which was not assisted by chasing a deep shot to the boundary), while Garner had become increasingly frustrated in his first permanent Willowfest outing with the gloves as his narrow failures to run out Hafeez and co. had closed in on double figures.</p>
<p>Collier was promoted to open the batting with O'Shea and, suitably liberated from ditching his pants, leapt for the skies to avoid copping a throw in the leg, only to watch the ball strike the stumps while still airborne. His wicket left 77 to get off 100 balls; no easy feat against an impressive opening bowling combination, which kept Garner scoreless until his eighth ball. At the other end, O'Shea was ticking along nicely, making sure the required run rate didn't get out of hand. The match changed dramatically in the eight over when 10-ball over was taken for 12 runs. Garner started to rotate the strike and O'Shea unleashed at the right time, sending the young leg spinner Tanner Begg's flighted delivery sailing over the midwicket boundary. The left armer Pretik turned the pressure back on, getting both batsman groping for off breaks with plenty of turn and bounce, but failed to capitalise when he put down Garner late in the chase. With the openers returning, the cue was put back in the rack and the target achieved with two overs to spare. For O'Shea, 44* and a Willowfest-best second wicket partnership left only a wicket out of his otherwise perfect day, while Garner could finally claim to have lifted the Mildura run monkey off his back.</p>
<p><i>N.b. To messrs Cetinich, Murdoch and O'Dwyer, you are lucky details of your dropped catches have been forgotten. Any assistance from the public in these matters would be greatly appreciated.</p></i>
