<p>For over four years, Josh O'Dwyer's entire Johnnie Walker XI contribution consisted of a run and two wicketless overs. One glorious week in Mildura quickly changed that. In the first T20 of Willowfest 2010, O'Dwyer launched an audacious attack on the SPL St Columbs bowlers, hitting six 4's and five 6's on his way to an unbeaten 78 off 43 balls. It was all the more remarkable considering his previous high in all cricket was just 27. His form from that match carried onto the following two days, including a late innings 20 to all but ensure the minor premiership and 19 in the Grand Final, when only one other batsman reached double figures. All the batting talk deflects from his stronger bowling suit, which has added a yard of pace since his debut. O'Dwyer took at least one wicket in all but one match during the carnival, while keeping the runs down at the same time. All things combined gave him a tight win in the Club Champion Award.<p>
<p>While his bowling remains most important to the Johnnies' structure, he will be keen to show this year that his efforts with the willow in 2010 were no fluke.</p>
