<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Semi Final v West Coburg</u></b></h3>  
<br></br>  
<p>The pre match preparation said much about the attitude of the two competing sides, and the boys were determined to prove that stuffing around in the middle of the park was a better way to reach a grand final than a preparatory net session. Having said that, a basic plan had been hatched by the Johnnies; namely to quell the league's champion player in Pas Triulcio, after seeing first hand the impact he could have on a match in the previous fixture between the two sides.</p>
<p>Chris Taylor was switching his tactics from the outset, replacing himself with Cooper Reid after just two overs of the innings in an attempt to have a batsman perish targeting the small square boundaries. After a double wide start, Reid coaxed Cranage into what seemed to be a firmly struck hoick over mid wicket but could only find Taylor on the fence. That brought Triulcio to the crease, who was immediately welcomed by several Ben Carr snorters. Carr, who has often done too much for the lesser batsman in the competition, couldn't even locate the edge of a higher class opponent in a probing display. An unchanged spell of 0/16 left West Coburg at 1/49 after 13 overs, at which point the Redbacks began searching for quick runs, only to find some testing bowling at the other end. Geoff Cox was getting the ball to swing sharply and was unlucky to have Triulcio dropped on 14 by Matthew Cross on the square leg boundary, whose excellent effort amounted to six runs. He would gain his reward next over, curling one in to the top of the left handed Caruso's pads for the umpire to adjudge to be hitting the top of off stump.</p>
<p>Cox and Mick Pataki kept the runs down in a tandem spell of ten overs, backed up by superb fielding, aside from Steve Ritchie's drop off Pataki's bowling, when Nathan Ward skied a slog sweep to short mid wicket. After 24 overs West Coburg were 2/93 and ready to hit out, but Pataki delivered a potential match-changing over with the key wicket of Triulcio. Most of the team were setting up for the next ball after witnessing David Bell 'settle' under the ball like a headless chook on the square leg boundary, but his hands proved to be sound, sparking big celebrations. When Ward swept Pataki to Taylor at short fine leg two overs later, the Redbacks were on just 98 with eight overs remaining and their three best batsmen back in the sheds. Inspired, Taylor ditched his short run up and immediately found plenty of pace and swing to trouble the batsmen, getting opposing skipper Drummond to poke a full toss back for a low diving catch. With the threat of the big hitters now gone, Reid was brought back and struck immediately when Gouliotidis gloved one to Carr's left at slip, via Vaughan Garner's fingertips and shoulder. An Andy Taylor on-the-run special gave Reid his third wicket, before Chris Taylor found himself on a hat-trick after a cheap wicket was followed by a huge inswinger that would have cleaned up better batsmen than a lowly competition number 10. A frustrating 19 run last wicket stand, ended by Steve Ritchie's only ball of the innings, set the target at a gettable 138 off 35 overs.</p>
<p>Garner and Pataki strode to the crease with the sole objective of keeping Truilcio and Ward out, however no one had bargained for the medium pacer Drummond sharing the new ball; perhaps altering his own tactics after seeing the movement on offer. He took the big wicket of Pataki off his seventh ball when this year's club champion feathered one behind. Carr, despite not being at his fluent best early on, knuckled down and with Garner took the score on to 30 in the 14th over. Having set a solid foundation, he fell lbw to a full ball from Cranage, but Kale Cetinich was not keen to drop the initiative, knocking his first ball away for four. By this stage, Triulcio and Ward had already sent down half their allotted overs, allowing Cetinich to free his arms at one end while Garner continued to block Ward out. A big six off from Cetinich off Cranage moved the chase up a gear, taking the score to 50 after 18 overs. 43 runs were added in 9.1 overs, during which Cetinich survived an extremely close run out shout. After conceding just six from his first four overs, Triulcio's second spell was lacking in pace and Cetinich took full toll, clubbing another big six to get the boys off their seats. Just when the bats were starting to take over, Garner played a loose cut shot off Surendrakumar to be caught for a patient 19, bringing the big hitters to the crease.</p>
<p>Cetinich continued on his merry way, taking the spinner to town with consecutive boundaries. His maiden half-century appeared imminent, but on 43 - his third JWXI score between 40 and 50 - a full Triulcio delivery breached his defence. With just 53 needed off 59 balls, and with six wickets still in hand, a massive collapse of 7/21 came at the worst possible time. Cox was trying hard with a couple of boundaries, however Billy Morrison and Reid's wickets in consecutive  Drummond deliveries put the pressure right on. Another double strike of Cox and Chris Taylor gave Drummond an unlikely 5-wicket haul and a second spell of 4/0 off eight balls. Bell hit the hat-trick ball away for three to maintain hope, but Andy Taylor was bowled next over, leaving 32 to get off five overs. Earlier in the season, Bell and Cross had feasted on a poor Sydenham Hillside attack to the tune of 68 off 5.3 overs, however there would be no such heroics against a high class opposition. Bell's dismissal attempting a suicidal single to mid off closing the match out, agonisingly short of a second grand final birth in under three months.</p>

