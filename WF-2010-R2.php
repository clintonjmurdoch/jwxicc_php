<h3 style=”text-align:left”><b><u>Willowfest 2010 - Round 2 v SPL St. Columbs</u></b></h3>    
<p>A belligerent evening the night before meant that most of the boys were feeling the pinch. The 9.30am start didn't exactly help things as we introduced ourselves to Gol Gol with more vomit than cheer. A decisive win on Day 1 however meant that confidence to get the job done in any conditions (or condition) was sky high. Matt Barclay won the toss and showed his creative side by sending Colin Finn in to open with Warren Sidebottom under clear skies. Sidebottom got off to a flyer with two boundaries in the first over, only to be adjudged leg before in the second. Vaughan Garner entered at first drop with the pressure of Willowfest history weighing him down, but he was able to relax quickly after witnessing Finn absolutely cream a ball off middle stump through the covers for four. An uncharacteristic tickle to the leg side to get Garner off the mark  brought about bronx cheers from devoted teammates. It must have upset the apple cart as Finn and Chris Barclay were cleaned up within the following three deliveries. Michael Dann carried his form from Day 1 over and immediately settled in, knocking the ball about with ease, but a promising partnership was thwarted as Garner - who passed 1 for the first time in five Willowfest innings - couldn't lift a lofted drive over mid off.</p>
<p>From a team perspective it was the best thing that could have happened as Josh O'Dwyer proceeded to play the innings of his life. After seeing off the opening bowlers, O'Dwyer - whose previous high score was just 27 - launched a savage attack against the hapless bowling attack. He contributed 24 out of a 33 run stand with Dann and 18 out of 29 with surprise packet Patty Lahn, whose baseball style brought him two boundaries and kept the score ticking over. Following Lahn's departure, O'Dwyer opened up, hitting opposition skipper Peter Horsford for 4, 6, 6, 2 and 1 in one over; the first of those sixes bringing up his maiden half-century off 30 balls. His dominance towards the end of the innings was so great that Andrew Dowell could manage just 5 off 7 out of a partnership of 45. All up, O'Dwyer finished with an unbeaten 78 off 43 - at the time the fourth highest score in JWXI history - with his last 75 runs coming from just 35 balls.</p>
<p>Defending 141, Clinton Murdoch got the team off to a great start with two early wickets to take his tally to six in two matches. Opening batsman Ravi Patel proved to be a thorn in the side however, riding his luck with some dashing strokeplay against Garner and Sidebottom to move the total quickly onto 2/67. With the run rate escalating, Chris Barclay went on the warpath with a spell of 3/3 in seven balls. He first conjured a false stroke out of nowhere from Prasanna, which was well snapped up by Finn at short fine leg. Three balls later Dann held onto a rocket at cover, and when he cleaned Shankar's stumps up next over, SPL St. Columbs were stuttering at 5/71. Patel was still doing as he liked however, carrying his team to 94 before one bold stroke too many off the irresistable O'Dwyer brought about his downfall.</p>
<p>The chase was still on while Horsford and Brendan Li were at the crease, as they continued to chip away at the target with some big hits and sharp running. Ultimately, it was running that brought about the downfall of the rest of the innings. First Garner, whose earlier attempt at a Jonty Rhodes style dive was just beaten by Li, effected an excellent runout to dismiss Horsford with a one handed pickup and backhand throw onto the stumps. Sensing the chase was completely up to him, Li embarked on a suicidal run to maintain strike without consulting his teammate, giving Matt Barclay an easy runout, and when Li perished  to another run out in the penultimate over, the game was as good as done.
