<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Round 13 v Sydenham Hillside</u></b></h3>  
<br></br>  
<p>Most of the boys were excited to take on the bottom placed team, however getting to the ground proved to be an ordeal full of umpteen back streets and seemingly a magic eye. The big hitters' eyes lit up when they first set foot on the ground, which was more like a postage stamp. It was no surprise then that Chris Taylor happily announced he would bat first after winning his fourth toss in a row.</p>
<p>Although the bowling was not the most threatening, Vaughan Garner and Ben Carr struggled to settle. Garner was given a life in the first over after the keeper spilled a leg side tickle, while Carr was deceived by a wobbler which breached his defences. Cooper Reid was promoted to the top three and instantly gave the innings some impetus with three boundaries in his first seven balls. Seemingly stable at 1/32, Garner then copped a similar ball to Carr and befell the same fate. It would be the last decent ball Sydenham Hillside delivered, or so the following events made it seem.</p>
<p>Reid hit another four and a big six before departing for 28; his second highest score made the innings after his highest. Kale Cetinich's delicious late cut was the lone highlight of his brief stay, which was ended after he failed to respond to a Mick Pataki call and narrowly lost out to a direct hit. This brought Billy Morrison to the crease, signalling the beginning of a record 113 run stand in under 15 overs. After surviving an early chance, Morrison chanced his arm and cleanly thumped a full ball on the stumps over the straight boundary fence. Sensing this as his best chance of scoring, he proceeded to play the same shot on almost every ball he faced. Having previously made just six runs in three JWXI innings, and possessing an all cricket high score of just 5, Morrison quickly found himself bringing up a maiden half century off just 45 balls. At the other end, Pataki did what he does best, picking off the bad balls and rotating the strike at will. By the time he departed for a solid 37 - strangely by missing a straight full toss that would have crashed into middle - the score had blown out to 176 with eight overs still remaining. Chris Taylor attempted to add a new shot to his repertoire after being convinced to try a switch hit, however he could only manage a limp switch pull straight to 'cover', gifting Sherlock his second wicket in his lone erratic over.</p>
<p>Morrison would not be distracted from his game plan. Having possibly played the shot of the year by belting one onto the clubroom roof, he chose the opposite end of the ground to unleash sheer brutality on the unlucky Barlas, whose yorkers Morrison was able to turn into half volleys asking to be sent over mid wicket. He went from 83 to 101 by drop kicking three consecutive balls over that mid wicket boundary, retiring after 67 balls of mayhem with the fastest century in JWXI history. David Bell and Matthew Cross then feasted on the disillusioned attack, adding a further 68 runs in the final 5.3 overs. The final result was a club record 7/284 and almost certainly a sixth straight Melbourne win (also seventh straight, and tenth out of eleven across all competitions).</p>
<p>Sydenham Hillside started the futile chase steadily, picking runs off surprise opener Andy Taylor as Carr moved the ball significantly at the other end. It was Taylor that took the spoils however, when Barlas tamely chipped a full toss to Cross at mid on, to take his first wicket in two years. The game meandered for a long period, to the point where Cetinich was given a new nickname. Having chased several balls to the boundary in the first ten overs, Garner began sledging him for not having done anything. At the same time, Chris Taylor was mentioning the famous Simpsons quote "the goggles, they do nothing!", resulting in Cetinich being called Goggles for the rest of the day. Cross restored order with the important dismissal of the dangerous Avarmoski with a legside ball that was gathered by Garner, who beat the overbalanced Avarmoski to complete the stumping. Wickets suddenly became a regular feature of the innings. Taylor picked up his first wicket since the last match before the christmas break after Reid staggeringly completed his second catch in as many matches (square leg tried to call a no ball for height but the standing umpire completely ignored his teammate), while Cross added another two to his tally - in a mini spell of 3/1 in nine balls - to finish with a well deserved 3/20. This included an excellent effort by Bell at point, who stretched to his right and held on to a good catch. Reid picked up his first wicket with the second ball of his spell, after gaining an lbw decision despite being the only appealer and the ball going over the stumps. Taylor's second wicket came about through his own field placement as Goggles pouched a fine catch low to the ground at backward short leg.</p>
<p>Looking for a quick kill at 7/92, the boys were only able to pick up one wicket in the final 10 overs as Alahakoon chipped away to build a respectable total. Reid was denied a second wicket on two occasions - both off Highgate - with Andy Taylor spilling a tough diving chance at mid on and Garner being denied a legside catch. Highgate would eventually fall in that fashion, with the umpire likely detecting an edge only when Garner didn't take the ball cleanly. As the game petered out, Alahakoon notched a half century, Morrison sent down a promising career-first over and Garner delivered the final over in a cap, keeping pads and box, surprisingly landing a few off and leg breaks. A prospective career change was ruined with Reid dropping a scorcher at square leg and Andy Taylor giving away five after Garner had moved him away from a potential catch.</p>


