<h3 style=”text-align:left”><b><u>Willowfest 2011 - Round 1 v Hutt River Dedication</u></b></h3>    
<br></br>
<p>For the long awaited first match at the quaintly named 'Onion Patch', we met Hutt River for the fourth time. Still smarting from the T20 loss in 2010, the boys were keen to reassert themselves on the Dedication. In a nod to the previous carnival's corresponding fixture, Matt Barclay sent the opposition in hoping for a quick kill. From a playing perspective, his job was done the moment he held the catch to remove Jarryd Mills. Like last year, Mills closed the face on a Clinton Murdoch full ball and sent it skywards. Picking up from his Team of the Carnival selection, Murdoch closed out a tight four over spell by bowling Chris Shaw, effectively cancelling out tough dropped chances by Josh O'Dwyer and Colin Finn. O'Dwyer picked up his own brace to reduce Hutt River to 4/20; first Finn uncovered his sound pair of hands to remove Joel McMahon, before big hitter Jake Sluiter was clean bowled.</p>
<p>Bill Barber and Tyler Hankinson steadied the ship as O'Dwyer lost his length, while Vaughan Garner couldn't find a length in his first bowl for some time. Barber was particularly savage on his shorter variety, clobbering a six over square leg. A 30 run stand was broken when Garner tossed up a back of the hand lollipop full toss, which Barber could only manage to lob to substitute fielder Ben Mason. Unhappy with the height of the delivery, Barber hung around, however a disappointed Mills could only shake his head and inform him that the ball had dipped <i>just</i> enough. Now requiring a legitimate wicket, Garner deceived Hankinson with an off cutter and took a good return catch falling to his left, shortly after seeing Rhys Collier put down a tough chance at cover from a similar delivery.</p>
<p>Michael Dann was introduced and quickly had Adam Burke well caught by O'Dwyer on the mid wicket boundary, before adding Chris Hope to his tally. With Sir Geoffrey Pascoe and Todd McCarthy (who became the fifth person to play for and against JWXI after a single outing in 2008/09) going into stonewalling mode, Dann gave his economy rate a nice polish, while Barclay used the opportunity to give some lesser lights a trundle, with Collier discovering some big swing to complement his ugooglies and Patrick Lahn rediscovering the touch that brought him two wickets on debut. Dann finally penetrated McCarthy's defence to round out a stunningly cheap three wicket haul, before Barclay deservedly filled in his column with the final strike.</p>
<p>As per tradition, Hutt River began with everyone in the slip cordon for the first ball, although Mills took the gloves to gully and, crucially, Sluiter sat behind the stumps, gloveless and wearing the worst performer suit jacket. On cue, Warren Sidebottom gloved one legside and Sluiter shelled a head high chance to his left; a relatively easy catch, if there is such a thing, for an unorthodoxly dressed wicketkeeper. O'Shea became the beneficiary of a fielding error next over when the usually safe Mills, having resumed his regular position, scuffed a high ball. Sidebottom could not capitalise on his good fortune, succumbing to Barber on 9, however O'Shea settled into a 37 run partnership with Dann. Mills avenged his mistake when O'Shea chased a wide McCarthy legbreak to be stumped by the length of the pitch, or so it seemed given his keenness to reach the ball. Any chance of a Dedication fightback was quashed with a quickfire 26 from Andrew Dowell, and even a trademark collapse of 3/6 couldn't alter the result. For the second time in three carnivals, the win came courtesy of a wide, giving Kyle Moore an infinite economy rate. Importantly, the 22 over chase provided a handy net run rate boost, which would factor into the final standings.</p>
