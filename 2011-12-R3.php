<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Round 3 v West Coburg</u></b></h3>  
<br></br>  
<p>A bright, warm day greeted the return of a number of regulars and a few new faces, including our 100th player Matthew Cross. The only problem was finding an opposition. With twenty minutes to go we eventually located seven West Coburg players in three separate groups. The Redbacks won the toss and chose to bat on a ground with a slow outfield and long square boundaries, but miniscule straight ones. Matt Lyons - the previous season's B Grade champion - took the new ball and showed his rust by bowling not one, but two consecutive beamers, one of which ran away for four. No mention was made of removing him from the attack, but when he repeated the dose two balls later, it was expected that it would be the final straw for West Coburg, yet somehow he escaped again, without even being warned. At the other end, Chris Taylor was getting the odd ball to swing in to the right hander with his short run. He eventually broke through with a peach of a delivery, beating Gouliotidis' defence and bending it back to nick the top of off stump. Four overs later a slower, fuller delivery was too much for Ross' attempted slog sweep, and by drinks West Coburg had limped to 2/58. A spate of dropped catches allowed the runs to flow (although they were welcomed to fund Mick Pataki's 6-pack for his 50 the week before), but 148 with only 9 (or maybe 10) fielders would prove tough to defend.</p>
<p>Ben Carr's early dismissal to a fairly average bowler failed to bring about any nerves as Pataki and Vaughan Garner settled in, comfortably negotiating the pre-drinks period and taking the score to 94, before a rare loose shot from Pataki set off a mini collapse of 3/12. Billy Morrison had a inauspicious debut with the bat, missing an attempted cut outside leg stump first ball, then tamely chipping the ball back to the bowler after being advised by Garner to play straight. A standard hit and get out innings from Andy Taylor brought brother Chris to the crease. Although there were more wickets in hand and less runs to get, the circumstances were eerily similar to the pair's astonishing partnership against Haig Amatuers two years before. Taylor proceeded in typical fashion by targeting the off spinner and hitting sixes at will. On this occasion however, Garner would not be denied a half-century by some late power hitting, as he stepped on the gas himself and pushed his Wallace Reserve average into the sixties. While not as damaging as the Haig match, 43 runs in five overs to win the match put the exclamation mark on a well deserved win.</p>

