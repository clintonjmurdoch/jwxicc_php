<h3 style=”text-align:left”><b><u>2006/07 - Round 3 v Mooroopna</u></b></h3>    
<br></br>
<p>This game would set the tone for future Mooroopna encounters – a competitive display combined with a narrow loss. The match got off to a great start with Warren Sidebottom winning the toss and electing to bat, before partnering Chris Taylor to smash the Mooroopna attack for 59 runs in well under 10 overs. Taylor’s dismissal for a quickfire 43 led to a collapse of 9/58, including Sidebottom for 25. James Hainsworth provided another useful contribution in the middle order with 21. Mooroopna’s chase started well, reaching 3/89, before a middle order collapse saw the total reach 8/111. The absence of Simon O’Shea would prove costly though, and the match soon soured for the JWXI, as Mooroopna reached the target with 2 wickets still intact, before being dismissed for 141. Vaughan Garner finally showed his potential with the fantastic figures of 8-1-13-3. Andrew Watt was expensive but still picked up 3 wickets, while Andrew Taylor took 2/12 off his 3 overs.</p>
