<h3 style=”text-align:left”><b><u>Team Rules</u></b></h3>
<p>These rules were written up before the Johnnies' first Melbourne season. Prize funds were frozen by differing opinions of the dropped catch rule and general analness, however in general the rules have been faithfully abided by for several seasons.</p>
<ol>

<li>There are no prizes for mediocrity - a $2 fine will be incurred for a dropped catch or a duck. Difficulty of catch dropped will be considered on the day, for example, a diving effort to get a finger on it will not be considered dropped for fine purposes.

Being hit for 6 is allowed as we are playing in a 35 over competition - hacks will be in their element, and let's face it; anyone can hit a good ball for 6 if they're lucky.</li>
<br></br>
<li>There are however prizes for a fair dinkum good effort, as follows:
<br></br>
<ul>
<li>50 runs / 5 wickets = 6 pack</li>
<li>100 runs / 7 wickets / hat trick = Slab</li>
<li>Amazing catch = negotiable, depending on a range of factors
(eg. big dive, 1 or 2 hands, landing on neck etc.)</li>
</ul>
<br></br>
<li>2 front foot no-balls within 2 overs means out of the attack, most likely for the rest of the match, and possibly a punch in the shoulder from each teammate. No-balls are unnecessary and very, very annoying - as well as giving up a hard earned run to the opposition - so for any critics of this rule, be glad that you're allowed 1.</li>
<br></br>
<li>Attack! Stonewalling has no place in both the JWXI and a 35 over one-day competition. We're playing on synthetic pitches on small grounds with limited time, so we need to get a wriggle on from the get go. There's a reason a batsman carries a piece of willow (or in some cases balsawood) to the middle, and it's not to chop down a tree or build a small fort.</li>
<br></br>
<li><p>Sledging. A well directed remark at the opposition is good, but a one-liner at your own teammate is priceless. Just because you're on the same team as someone, doesn't make that person any better at the game than the XI on the other side of the fence, so make sure no mistake goes unnoticed - especially if that XI is too quiet or stupid to say anything. Plus, for some unknown reason, it feels a hell of a lot funnier as well.</p>

<p>Previous example - first delivery of the 2006/07 season bowled by Christine Naylor* (first game of the soon to be named JWXICC), ball is struck to point, where the fielder Dawn Farmer* inexplicably flicks the ball over her own head for 4 in an attempt to knock it back to backup fielder Simone O'Dea*. Unknown player* remarks "Well this is going to be a good season isn't it".</p> 

<p>(* players names and genders have been altered to protect identity)</p>
<p>(P.S. JWXICC actually went on to notch it's maiden victory in the same match, proving that sledging your teammate will not break his spirit)</p></li>
<br></br>
<li>11 bowlers, 11 batsmen. Racial equality may not have been reached yet in this country (Cronulla riots) but cricketing equality is well and truly alive in the JWXI. Everyone will get a go regardless of skill level, so don't throw a wobbly if you only bowl 4 overs. Besides, in a 35 over innings you can only bowl 7 overs anyway.</li>
<br></br>
<li>Here's to drinking. You might get a go regardless of skill level, but you only need to look as far as the name that the team bears to realise that drinking is essentially what the team stands for (never during a game though, original members will know the repurcussions of such incidents). This is not a rule per se, but it is a recommendation that a player of the JWXI attends all pre and post-match events with a typical Australian swagger, and doesn't use the excuse "But we're playing tomorrow". Arguments have been raised about the effects of drinking before a game, but there is enough evidence to suggest that performance is improved. And even if it's not, it's still more entertaining than a quiet Saturday afternoon.</li>
</ol>
