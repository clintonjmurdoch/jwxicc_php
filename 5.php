<p>If an all time Johnnie Walker XI were to be picked now, Simon O'Shea would make it as both a reliable batsman (possessing a sumptuous leg glance) and a parsimonious yet penetrative medium pacer. Initially resisting calls to leave his home club Central Park/St. Brendan's, O'Shea was eventually persuaded to join the fledgling club. The move paid immediate dividends, as he took three wickets in each of his first two matches, while he was arguably the best player in three of the first four matches he played. A consistent season in which he took a wicket in all but two bowling innings and reached double figures 11 out of 14 times saw him take out the champion player award, the bowling award with 22* wickets (comfortably an all time best) and narrowly miss out on the batting award while accumulating a team high 298 runs. If not for poor data entry on MyCricket, O'Shea would have edged the entire club's champion player award as well.</p>
<p>A return to his old club and high level cricket over the following two years prevented O'Shea from making a Melbourne comeback, however he was one of the first players to offer his services for the first Willowfest campaign. In a tournament where several players struggled for runs, O'Shea managed 55 (fourth highest), while his bowling - although not called on often - remained first class, aside from his brief flirtation with straight break bowling, when he was hit for a massive six over midwicket at the main Red Cliffs ground before somehow removing the same batsman plumb leg before.</p>
<p>The runs dried up during Willowfest 2010, however his stoic efforts at the top of the order consistently removed the shine from the ball and allowed following batsmen to build a solid score, while also wiping his first over drop of Jarryd Mills from teammates' consciences (although had Mills made even 50 no one would have ever forgotten). His seven wickets also came at the stingy rate of 2.51 runs an over, confirming his status as the preeminent JWXI bowler, and his performances at his impending third carnival in 2011 will be critical if the JWXI are to finally taste grand final success.</p>
