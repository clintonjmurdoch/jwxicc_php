<p>Rory Herd was a key village recruit for the first Melbourne season. A quick left-arm bowler and hard hitting lower order batsman, Herd gave key contributions to victories in his first two matches. Following an economical spell on debut, he combined with Vaughan Garner for a then record 68 run stand for the seventh wicket to settle the game, before taking two wickets and hitting a dashing unbeaten 33 to ice a 229 run chase the next week. Consecutive ducks in his next two matches put a dampener on his barnstorming start and he would not be seen for eleven matches, when another late order effort in a failed chase was small consolation for a spell of 0/61 - the most expensive figures in club history.</p>
<p>After being absent for almost three years, Herd made a surprise return in early 2012, picking up key top order wickets en route to a career best 2/12 and helping set up a big win.</p>
