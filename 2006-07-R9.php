<h3 style=”text-align:left”><b><u>2006/07 - Round 9 v Old Students</u></b></h3>    
<br></br>
<p>While it may look as though this match was a replacement for the cancelled Round 8 encounter, departing teams had in fact left holes in the fixture and, as a result, the fixture became ridiculous. The JWXI bowlers were punished in this match, as Old Students racked up 5/269 in 40 overs. Dale McNair and Chris Murphy each picked up 2 wickets. In reply, JWXI didn’t put up a fight – all out for 88 in 27 overs. Andrew Watt defied the opinion that he couldn’t bat, smashing 22 from number 10, including 2 massive sixes over midwicket. His innings drew the ire of teammates however, who wanted to depart the lost cause as quickly as possible for the traditional post-match Hungry Jack's meal/food fight.</p>
