<h3 style=”text-align:left”><b><u>2006/07 - Round 10 v Pine Lodge</u></b></h3>    
<br></br>
<p>Skills were rusty following the Christmas break, however the batsmen were still able to chalk up 142. Greg Sidebottom continued his lower order heroics with a tidy 28 not out, with contributions from Andrew Taylor and Jeremy Watt. Chris Taylor’s stay was brief, however he did not disappoint the crowd, belting one ball over midwicket into the scorer’s lap, before annihilating a straight hit into the nearby croquet club. Pine Lodge then got off to a brisk start, before the middle order capitulated from 2/74 to 95 all out. Simon O’Shea took 4/15 and debutant Ashley Keating provided much needed variation into the attack – his leg spin snaring him 4/20. In a game that had everything, Colin Finn announced himself to the cricket world with his only 3 runs for the season and a stunning low running catch, veteran John Watt was caught plumb in front by the opposition keeper Steven Pigatto, Jeremy Watt almost got into a fight after sledging his old enemy in Pigatto following his dismissal, Vaughan Garner proved that a cast is no barrier, taking a wicket and a catch with one arm, and Matthew Barclay used his week off from A Grade to take a catch as a substitute fielder as the side chalked up an impressive win. </p>
