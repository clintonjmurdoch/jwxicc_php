<h3 style=”text-align:left”><b><u>Willowfest 2010 - Round 3 v Hutt River Dedication</u></b></h3>    
<p>After the first meeting between the two heavyweights of privateer cricket clubs was taken relatively seriously, the second meeting commenced with a more village-like atmosphere, however the small crowd were still perplexed to find Rhys Collier - into the side to replace the resting Chris Barclay - bowling to Kyle Moore and Warren Sparks, who had made three runs between them from No. 10 and 11 the previous morning. With Collier unable to bowl his ugooglies, it appeared that even a one armed newborn would be able to decipher his slow medium pace, and so it was confirmed with a no ball, wide and two start. Then out of nowhere, Collier was able to land it on a good length and penetrated Moore's defence to claim a wicket with his fourth - and second legal - JWXI delivery. Three more wides rounded out one of the more strange overs of recent memory - 11 balls long in all. Michael Dann surprisingly shared the new ball and, apart from catching Collier's wide disease, proved to be quite economical, bowling Sparkes with the final ball of his nine ball opening over; an unchanged spell netting him the excellent figures of 3/12.</p>
<p>With the hit and gigglers out of the way, the blokes that reckoned they could actually play a bit took centre stage. Kale McBain failed to repeat his Day 1 salvage mission, succumbing to O'Shea's first delivery for 1. At 5/39 another capitulation was on the cards, but Jake Sluiter and Adam Burke were able to steady the ship while adding 34 in six overs. O'Shea removed Burke for a well compiled 22, caught behind off Warren Sidebottom, who had taken the gloves in anticipation of Andrew Dowell's maiden spell. After a beamer first ball, Dowell somehow managed to entice an edge off Joel Thomas, which Sidebottom gleefully swallowed, to upstage both Dann and Collier's earlier efforts in taking a wicket in a maiden JWXI over. That was as threatening as Dowell got, apart from a second beamer to force his removal from the attack after four consecutive boundaries, finishing with the bizarre figures of 1/27 off 1.4 overs. Clinton Murdoch used his late introduction to claim another three wickets - including a gift from Sir Geoffrey Pascoe, who tried to lift a yorker on middle stump over Dowell at fly slip on the final ball of the innings - but with Hutt River sprinting to 139, the match had turned serious.</p>
<p>Collier joined Sidebottom and confirmed his love of new balls by belting a couple of boundaries in the second over, before falling leg before to Chris Hope for 8 on the <i>seventh</i> legal ball of the over. Sidebottom and Dann then came together to take JWXI within striking distance of the target, putting on 55 in 6.5 overs. Sidebottom's dismissal didn't result in a collapse, but a steady flow of wickets slowed the runs at one end and left Dann - who had cruised to a 43 ball half-century - with a lot of work to do. With 13 to get off 10, he tried to go big against Pascoe's spin and failed. O'Shea suffered the same fate next ball, but with 11 required from the final over and three wickets in hand the game was still on. Hearts sunk when Matt Barclay was trapped by Thomas, and when Dowell was run out first ball going for a third run, it was left to Murdoch and Patty Lahn to get the job done. A two to Murdoch gave JWXI some hope, but a thrilling game finally finished in Hutt River's favour with Pascoe catching Murdoch on the long off boundary.</p>
