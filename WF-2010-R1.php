<h3 style=”text-align:left”><b><u>Willowfest 2010 - Round 1 v Hutt River Dedication</u></b></h3>    
<br></br>
<p>For the second year in succession, our opening Willowfest encounter matched us against Jarryd Mills' Hutt River, but thats where the similarities ended.
Simon O'Shea putting down Mills in the first over threatened to dampen spirits, but with Clinton Murdoch tightening the screws at one end, it was only a matter of time before another chance was created. As it turned out, it wasn't the bowling that did the trick but the pressure took its toll on young Mills, who lost his head after his skipper Sir Geoffrey Pascoe had blocked a ball straight back to Murdoch and called him through for a single. Murdoch accepted the gift and affected the runout with Pascoe left perplexed in the middle of the pitch, unable to get back after Mills had scootered past him. Murdoch needed no more invitation to tear through the batting lineup. Two runs later Mills' attempted leg glance took a leading edge for Andrew Dowell to accept a simple maiden offering with the gloves at short square leg, and when Hutt River's star in Joel McMahon smacked a cut shot straight to Vaughan Garner at point, the dedicated ones were in disarray at 3/19. Tyler Hankinson couldn't keep a booming cover drive down with Chris Barclay somehow holding on, and after clean bowling Jake Sluiter, Murdoch had the startling figures of 4/9 from seven overs. The first of Josh O'Dwyer's two wickets an over later left Hutt River reeling at 6/28. A 24 run partnership between Joel Thomas and Kale McBain held off the inevitable, but after McBain perished to O'Dwyer going for a third six - smartly caught on the boundary by Michael Dann - the wheels had well and truly fallen off. O'Shea and Matt Barclay polished off the tail and we were left with 67 to get from just 50 overs...</p>
<p>The chase got off to a shocking start with Matt Barclay triggered for a five ball duck, but with O'Shea building a fort at one end, Dann was able to come in and play shots immediately. He had cruised to 33 before he was run out in sight of the target. Dowell's debut innings was one to forget as he managed to get dropped and run out on the same ball for a three ball duck, and would surely have been wearing the purple G-String on Day 2 had Willowfest 2009's tradition been continued. Warren Sidebottom ensured there would be no late collapse with a calm knock with the chase wrapped up inside 20 overs.</p>
