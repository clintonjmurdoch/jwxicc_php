<h3 style=”text-align:left”><b><u>2009/10 - Round 2 v Pascoe Vale SC</u></b></h3>    
<br></br>
<p>A strong team was named for the clash against fierce rivals Pascoe Vale and, after a narrow victory in Round 1, confidence in the camp was high. Ben Carr (all-rounder), Johnny Derwent (legspinner) and Andrew Hewitt (batsman) were handed their maiden caps, and although only one was used specifically for their role, they all played a part in an amazing success.</p>
<p>Despite fielding a team of just eight players, Pascoe Vale got off to a roaring start when Jarryd Mills somehow ducked under a full toss and was bowled^. Dolf Reid at square leg called a no ball for a waist high full toss, which was vigorously argued by the opposition. A short delay ensued until Vaughan Garner told the standing umpires to give it out, having experienced a similar event and been told by a senior umpire that it should be given out. Mills was understandably livid, and would later indirectly get revenge on Garner. Another promotion for Clinton Murdoch proved farcical, lasting just three balls, and when Marshall Richardson was dismissed for just 5, JWXI was struggling at 3/23, with Hewitt showing the only signs of resistance. Garner had faced just three balls when Pascoe Vale went up for a ball that glanced his thigh pad, however Mills upheld the appeal. Hewitt's great effort ended on 24, bringing Dolf Reid to the crease to join Derwent. The pair added a run a ball 30, before Derwent departed for 27. Reid continued to hit out with Chris Taylor and Steve Auchettl for company, but when he fell on 128 the end was nigh.</p>
<p>Cooper Reid and Carr had other ideas however and, having quickly decided that offence was the best form of attack, proceeded to belt the Pascoe Vale attack to all parts. Reid in particular was in the zone, smacking five 4's and a six on his way to 28 off 19 balls, while Carr proved to be no slouch either with 26 off 22. In seven overs of carnage the pair had put on a JWXI best 55 for the last wicket; bettering the previous mark of 31, set by Garner and Chris Murphy in the very first match.</p>
<p>With a small batting lineup, 184 was always going to be a stiff chase, and the match was effectively finished within the first four overs. Carr provided arguably the highlight of the match first ball, delivering a bullet throw to Mills to consign Sutton to a rare black diamond* duck. Murdoch and Dolf Reid then each shared a wicket to leave Pascoe Vale tottering on 3/8. Reid's wicket was as much strange as it was vital - Ross Matthews being one of the best players in the competition - with brother Cooper clutching his first ever catch at first slip off a Mills rebound. Pascoe Vale went down swinging, but regular wickets meant they had no realistic hope. Fittingly, man of the match Cooper Reid picked up two of the final three wickets; including the last, taken expertly by Mills standing up to the stumps.</p>
<p><i>^ Later research confirmed that a full toss above waist height hitting the stumps unimpeded should remain a no ball, however the consulted expert determined that such an event would be extremely rare - even for the height challenged Mills. In summation, Mills should have hit it for six.</i></p>
<p><i>* While this author believes a first ball of an innings dismissal to be a black duck, several sources on the internet give various names, such as platinum or ruby. Even diamond duck is argued, however this author believes them to be retarded.</i></p>

