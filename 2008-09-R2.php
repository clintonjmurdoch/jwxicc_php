<h3 style=”text-align:left”><b><u>2008/09 - Round 2 v Pascoe Vale SC</u></b></h3>    
<br></br>
<p>This match would showcase the complete spectrum of abilities of the Johnnie Walker XI. Much of the team were surprised to learn that they would be playing cricket on arrival at a ground that looked more at home on the 9th hole at Augusta. Vaughan Garner lost another toss as Pascoe Vale elected to tee off first. The opening breakthrough came in the first over. After a sloppy overthrow from Garner got Sutton off the mark, Clinton Murdoch (7-0-1-38) knocked over Aslett first ball – a cunning strategy. The score moved quickly to 73 as the bowlers struggled to deal with a ragged fielding display. It would take something extraordinary to remove one of the set batsmen, and few would have believed what they saw as Sutton skied an Ashley Keating (7-0-2-41) leg-break to midwicket. Jarryd Mills set off in hot pursuit – helmet and all – for the ball, which was hovering directly over Chris Taylor’s path. As Taylor attempted the catch, Mills crash tackled him in a style more akin to the NFL. Hearts were in mouths as Taylor lay motionless on the ground for a few seconds, before rising up for a celebratory lap – ball safely in hand. That set off a collapse of 4/19, however the young number three Grutteria would not be moved, stroking an excellent 78 – his only moment of weakness coming when Mills spoke from behind the stumps of ‘ranga love’. He eventually fell to a trademark stunner of a catch from Andrew Taylor off his brother’s bowling. Pascoe Vale closed their innings on 8/228 – a competitive but achievable target on a small and fast outfield.</p>
<p>The reply was swift from the outset, with Garner and Mills putting on 55 in quick time, before Garner (23 off 26) gave his wicket away with the bowling at JWXI’s mercy. That only served to spur the team further as Ben Perry joined Mills at the crease, in one of the most extraordinary partnerships in the team’s brief history. While Mills accumulated his runs with typical panache, Perry – whose entire cricket career consisted of a golden duck – edged the Pascoe Vale attack to distraction. Countless attempted drives managed to find their way over, under and through the slips cordon, an invisible crease saved a certain run out and even one delivery that penetrated Perry’s defence disturbed the stumps but refused to do likewise to the bails. Following the overdue plugging of the slips cordon, Perry learnt how to bat, smacking an A-Grade bowler (whose name shall not be mentioned here) straight down the ground for yet another boundary. By the time he was finally out for 44 off 47 balls, 110 runs had been added - the second highest partnership in JWXI history - and the target was well and truly within reach. Mills had fallen shortly before after playing a tired shot, for an excellent 65. 62 runs were still required, but with Rory Herd (33* off 25) replicating his Round 1 form and debutant Marshall Richardson providing a calm hand, the target was reached with five balls to go.</p>
<p>The Johnnies were over the moon, sitting pretty on 2-0, however the joy was short lived as the result was overturned, due to the fact that no one had realised that Perry had played for Sunshine YCW the previous year. A win is still a win though, and nothing could ruin such a brilliant chase.
</p>

