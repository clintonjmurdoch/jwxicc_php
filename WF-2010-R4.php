<h3 style=”text-align:left”><b><u>Willowfest 2010 - Round 4 v Kangarilla B</u></b></h3>    
<p>Our first match against Kangarilla B was shaping as a grand final preview; Kangarilla were undefeated and assured of a berth, while only a bad loss would allow Hutt River to leapfrog us. Matt Barclay won the toss and chose to bat on an true deck, getting off to a solid start with Simon O'Shea. Despite their wickets within two overs of each other, contributions from Michael Dann and Warren Sidebottom ensured the innings didn't fall away, while freeing up Andrew Dowell at the other end. Dowell had experienced a torrid time with the bat up to this point, scoring just seven runs in the first three games, and was keen not to let a golden opportunity go to waste. He paced his innings superbly, scoring at a run a ball to start with, before gradually accelerating during the middle stages of his innings, bringing up his 50 off just 37 balls. At one stage he had a sequence of 12 scoring shots from 13 balls, indicating the level of dominance he had over the Kangarilla bowling attack. When he was eventually dismissed for 73, JWXI had reached 6/143 with 17 overs still to bat. Josh O'Dwyer unveiled some more big hits, while the rest of the lineup focused on crease occupation, pushing the score to 198.</p>
<p>Kangarilla got off to a roaring start, reaching 44 without loss in the eight over - as Chris Barclay struggled with no balls and Clinton Murdoch for once couldn't find an early breakthrough. The introduction of Matt Barclay, as well as a curious umpiring decision from Rob Lauder, changed the course of the match. After dismissing Travis Gordon, Barclay drew Michael Ross into an errant shot and set himself up for an easy return catch, only to muff it. While the ball was in transit however, Ross was exuberantly calling for a second run; so much so that umpire Lauder called him aside and informed him that he was out, despite the lack of an appeal. Once it was apparent that he had been dismissed for obstructing the field, the boys went crazy in celebration; a sound square-up for the hitting the ball twice dismissal that had not been given the previous year against Sunraysia Daily. Amid the confusion, scorers noted the wicket as a question mark; unaware of why their batsman was leaving the field.</p>
<p>Two balls later, Josh Ross, who had been big noting himself throughout JWXI's innings, edged a Barclay delivery wide of Sidebottom at slip, who clutched an absolute screamer. Barclay finished with 2/5 off six overs, and when O'Shea joined the party with two wickets, Kangarilla were down and out at 5/75. O'Shea's 10 overs cost just 1.8 runs each, bowling in excellent partnerships with Barclay and Vaughan Garner. With the match sewn up, Dann was brought into the attack for some off spin and struck immediately to give Sidebottom a second catch. O'Dwyer struck four balls later to hasten the end, and the following over Dann had his second when he lured Hiren Shah out to give Dowell his first JWXI stumping. With two wickets in hand, Mitchell White played a couple of big shots to damage Dann's figures slightly. Any chance of a fightback was destroyed when Garner cut off a boundary bound ball from Troy Martinsen by diving in mid air, palming the ball back into play and recovering to throw the ball back to Dann to find Martinsen well short of completing his third run. Dann's second consecutive three wicket haul was completed in his next over when Garner held on to a high slog sweep from White.</p>


