<h3 style=”text-align:left”><b><u>Willowfest 2011 - Round 2 v Kangarilla B</u></b></h3>    
<br></br>
<p>Looking to exact revenge for the 2010 grand final loss, the boys were up against it after Kangarilla won the toss and batted on a true Red Cliffs surface. Andrew Dowell's miss behind the stumps off Clinton Murdoch gave the reigning premiers a breather as they moved comfortably to 44 without loss after eight overs. It wasn't a happy morning for Murdoch, who joined Simon O'Shea's one man club of JWXI players to have been hit onto the distant Quandong Park 3 hill. Josh O'Dwyer broke through for the first scalp, but he too would fluff a chance in the deep off a luckless Michael Dann, who also saw Vaughan Garner spill a tough dving chance. In a sign of things to come later in the day, O'Shea showed a sure pair of hands to hand Dann a wicket, taking a skied ball at mid off. Garner's average start with the ball continued, although he did manage to induce another hoick - this time to short fine leg - to give Colin Finn his second catch of the carnival. The run flow did not abate however; Kangarilla going on to a competitive 3/135. In two previous T20s at the venue, the highest score was just 76.</p>
<p>The chase got off to a poor start when Willowfest debutant Kale Cetinich late cut a ball straight to the man at short gully, departing for a duck. Matt Barclay also fell without scoring, stalling his carnival tally at two runs from as many hits, before Warren Sidebottom's bright start was ended on 19, leaving the score at 3/24. Dann and O'Dwyer briefly returned to their form of the previous year and threatened a dangerous counterattack, but their dismissals within five balls of each other - at the hand of Troy Martinsen, the most bizarre but comfortably the least threatening of all the Kangarilla bowlers - effectively ended the game. O'Shea and Dowell also fell to Martinsen, who completed the unlikely tidy figures of 4/17 from his full complement of overs. When Finn's 13 ball stay ended, a massive loss was on the cards, but Pete Barclay and Garner were able to put on 21 for the ninth wicket to give the innings an air of respectability. Murdoch's three ball cameo was focused entirely on removing himself from the hill club by hitting a six there himself. His second ball failed by mere metres, however the deep boundary still meant that a boundary four was the result.</p>
