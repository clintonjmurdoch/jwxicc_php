<h3 style=”text-align:left”><b><u>2006/07 - Round 17 v Old Students</u></b></h3>    
<br></br>
<p>After it became apparent that JWXI had next-to-no chance of making finals, captain Warren Sidebottom took the opportunity to give those who hadn’t bowled much during the year – including himself – a go with the ball. This seemed dangerous against the top side, yet Sidebottom defied the critics as he shed the gloves, took the new ball and returned dazzling figures of 4/19 from his 8 overs, as Old Students were reduced to 6/57. They would eventually reach 204, but not before Colin Finn stunned the crowd with an analysis of 8-1-34-3, figures which included the scalps of Matthew Barclay’s brother and father; the latter of which was caught by Andrew Watt, completing the most unlikely bowler-fielder combination. Perhaps the batsman couldn’t see the red ball coming from Finn’s glowing locks. The batting was simply dismal, as the innings crashed to 88 all out in under 20 overs. Simon O’Shea provided the only resistance with 26, however Chris Taylor stole the show as he attempted to get insurance companies involved in the game; a flat six over midwicket missing passing cars by inches. </p>
