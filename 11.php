<p>Recruited under the brother-brother rule courtesy of his relation to Jeremy, the boy known as Fluffy was an integral member of the original JWXI four pronged pace attack. Watt proved to be a consistent wicket taker, as an average of a wicket per match attests, however his unrivalled ability to bowl a front foot no ball prevented him from bowling lengthy spells and, ultimately, becoming an all time JWXI great.</p>
<p>A traditional slogger of a tailender, Watt belted almost half his career runs, and a quarter of the innings total, towards the end of a lost cause against Old Students at McGregor Oval in late 2006. While it was an entertaining display, he didn't please his teammates, who were eager to depart for the traditional Hungry Jack's post-match dinner. For a man of his size, Watt was a suprisingly agile fielder at fine leg, where he pouched a number of tough chances, but he was utterly useless elsewhere.</p>
