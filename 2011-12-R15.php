<h3 style=”text-align:left”><b><u>NWMCA OD3 2011/12 Round 15 v Clarkefield Brumbies</u></b></h3>  
<br></br>  
<p>The equation was simple going into this clash - win and take the minor premiership. It would prove easier said than done, as Clarkefield gave us arguably our biggest cricketing lesson since the Willowfest Grand Final loss against Kangarilla B in 2010. Chris Taylor won yet another toss and, as he and Ben Carr needed to leave early for brother Andy's engagement party, chose to bowl so they could fit their overs in. With 13 players, numbers weren't an issue, however the addition of two big hitting batsmen may have altered the course of the match.</p>
<p>The day started smoothly with Carr bowling tidily and Taylor bowling a bit of licorice but remaining economical nonetheless. Taylor almost struck early when Main lobbed one to a diving David Bell at deep mid off, but regular chances were hard to come by as Clarkefield took a steady approach. Consistent spells from Geoffrey Williams - playing his first match in a year - and Steve Ritchie maintained the pressure, however Williams missed another chance, spilling a tough return chance - again off Main. Taylor made the breakthrough in the 16th over with one of the worst balls ever witnessed in the club's history. A very short, very wide ball caused Guneratne's eyes to light up, but he could only manage to toe-end it straight to Carr at a wide slip. Carr almost made it two in quick time, but couldn't hold onto another difficult caught and bowled. Williams would take the second wicket in the 23rd over when Murray clipped a full ball to Mick Pataki at short mid wicket, and when Cooper Reid removed Main for 59 - caught by Williams at deep mid wicket - a low score was on the cards. Reid missed out on a second wicket when he put down another return offering; this time low to the ground, unlike the first two. Wickets to Matthew Cross and Pataki left Clarkefield on 5/122 with just four overs remaining, however some brutal hitting from Heydrich and Buttigieg pushed the total to 176.</p>
<p>Pataki and Geoff Cox started cautiously, but Cox wasn't able to get through the first five overs, falling for under 20 for the first time in his short JWXI career. Kale Cetinich entered with just 14 runs from three post-Christmas innings, but looked technically sound in his short stay, defending well and hitting one strong, straight boundary. Though he only scored 8, signs are good that his best may yet return for the finals. Garner then joined Pataki at the crease, but the two leading run scorers struggled to push the score on, adding just 16 in seven overs. Garner in particular became frustrated as two boundary-bound shots were cut off by some excellent fielding. The scoreboard pressure led him to attempting a lofted on drive, but a slower ball forced the shot to the sole outfielder at deep mid wicket. Bell was bowled shortly after, and when last start centurion Billy Morrison was dismissed by a one handed pearler at slip, the match was as good as over. Strangely, all five wickets had fallen on the fifth ball of an over, perhaps showing the frustration stemming from the lack of easy runs.</p>
<p>By this stage, Pataki was suffering from severe leg pain which didn't allow him to play big strokes. Cross attempted to put together a partnership but he too skied an easy catch. Reid then fell in comical fashion when he was too slow getting back into his crease and was stumped by a direct hit from Main. Pataki was next to fall for a laborious 31, spurning the chance to become the first Johnnie player to carry his bat for the second time in 2011/12. Williams was the last man dismissed, becoming the sixth batsman to fall for 2, but Steve Pataki provided the highlight of the innings with a run-a-ball 2 after chopping a wide ball to third man. Steve Ritchie was also unbeaten in his first hit for the Johnnies.</p>

